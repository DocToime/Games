function isGameDataFormattedCorrectly(gameData) {
    for (let difficulty in gameData) {
        if (typeof gameData[difficulty] !== 'object') {
            console.error(`Error in difficulty '${difficulty}': Should be an object.`);
            return false;
        }

        for (let level in gameData[difficulty]) {
            const questions = gameData[difficulty][level];
            if (!Array.isArray(questions)) {
                console.error(`Error in level '${level}' of difficulty '${difficulty}': Should be an array.`);
                return false;
            }

            for (let question of questions) {
                if (typeof question !== 'object' || !Array.isArray(question.words) || typeof question.correct !== 'string' || typeof question.hint !== 'string') {
                    console.error(`Error in a question in level '${level}' of difficulty '${difficulty}': Question should be an object with 'words' (array), 'correct' (string), and 'hint' (string).`);
                    return false;
                }

                if (question.words.length === 0) {
                    console.error(`Error in a question in level '${level}' of difficulty '${difficulty}': 'words' array should not be empty.`);
                    return false;
                }
            }
        }
    }

    console.log("GameData is formatted correctly.");
    return true;
}

